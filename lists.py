# A List is a collection which is ordered and changeable. Allows duplicate members.
fruits = ['apple', 'banana', 'cherry']

print(fruits)
print(fruits[0])
print(fruits[1])
print(fruits[2])

fruits.append('orange')
print(fruits)

fruits.remove('banana')
print(fruits)

fruits.sort()
print(fruits)

fruits.reverse()
print(fruits)
